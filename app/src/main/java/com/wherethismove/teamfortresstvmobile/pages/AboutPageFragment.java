package com.wherethismove.teamfortresstvmobile.pages;

import android.content.Context;
import android.net.Uri;
import android.os.Bundle;
import android.support.v4.app.Fragment;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.TextView;

import com.wherethismove.teamfortresstvmobile.BuildConfig;
import com.wherethismove.teamfortresstvmobile.R;

public class AboutPageFragment extends Fragment {

    private OnFragmentInteractionListener mListener;

    public AboutPageFragment() {
        // Required empty public constructor
    }

    public static AboutPageFragment newInstance() {
        AboutPageFragment fragment = new AboutPageFragment();
        Bundle args = new Bundle();
        fragment.setArguments(args);
        return fragment;
    }

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container, Bundle savedInstanceState)
    {
        // Inflate the layout for this fragment
        View v = inflater.inflate(R.layout.fragment_about_page, container, false);
        String version = BuildConfig.VERSION_NAME + "("+BuildConfig.VERSION_CODE+")";
        TextView version_tv = (TextView) v.findViewById(R.id.version_number);
        version_tv.setText(version);

        return v;
    }

    // TODO: Rename method, update argument and hook method into UI event
    public void onButtonPressed(Uri uri) {
        if (mListener != null) {
            mListener.onFragmentInteraction(uri);
        }
    }

    @Override
    public void onAttach(Context context) {
        super.onAttach(context);
        if (context instanceof OnFragmentInteractionListener) {
            mListener = (OnFragmentInteractionListener) context;
        } else {
            throw new RuntimeException(context.toString()
                    + " must implement OnFragmentInteractionListener");
        }
    }

    @Override
    public void onDetach() {
        super.onDetach();
        mListener = null;
    }

    public interface OnFragmentInteractionListener
    {
        void onFragmentInteraction(Uri uri);
    }
}
